package SoftEng_2024.Model.Enums;

/**
 * Enumeration representing colors.
 * <p>
 * The colors included are:
 * - {@code RED}
 * - {@code BLUE}
 * - {@code GREEN}
 * - {@code YELLOW}
 * - {@code BLACK}
 * - {@code EMPTY}
 */
public enum Color {
    /**
     * Represents the color Red.
     */
    RED,

    /**
     * Represents the color Blue.
     */
    BLUE,

    /**
     * Represents the color Green.
     */
    GREEN,

    /**
     * Represents the color Yellow.
     */
    YELLOW,

    /**
     * Represents the color Black.
     */
    BLACK,

    /**
     * Represents an empty or unspecified color.
     */
    EMPTY;
}
